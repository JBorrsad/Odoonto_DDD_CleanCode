namespace Odoonto.Domain.Models.ValueObjects
{
    /// <summary>
    /// Representa el género de una persona como enumeración
    /// </summary>
    public enum GenderType
    {
        /// <summary>
        /// No especificado
        /// </summary>
        NotSpecified = 0,

        /// <summary>
        /// Masculino
